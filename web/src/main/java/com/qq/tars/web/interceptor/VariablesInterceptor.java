package com.qq.tars.web.interceptor;

import javax.servlet.http.HttpServletRequest;
import javax.servlet.http.HttpServletResponse;

import org.slf4j.Logger;
import org.slf4j.LoggerFactory;
import org.springframework.beans.factory.annotation.Autowired;
import org.springframework.web.method.HandlerMethod;
import org.springframework.web.servlet.HandlerInterceptor;
import org.springframework.web.servlet.ModelAndView;

import com.qq.tars.service.SystemConfigService;
import com.qq.tars.service.monitor.TARSStatMonitorService;

public class VariablesInterceptor implements HandlerInterceptor {

    @Autowired
    private SystemConfigService systemConfigService;
	
	
	private final Logger log = LoggerFactory.getLogger(VariablesInterceptor.class);
    @Override
    public boolean preHandle(HttpServletRequest request, HttpServletResponse response,  Object handler)
            throws Exception {
    	String traceUrl=systemConfigService.getProperty("trace.url");
		request.setAttribute("trace_url", traceUrl);
		log.info(String.format("  traceUrl is %s ",traceUrl));
        return true;
    }

    @Override
    public void postHandle(HttpServletRequest request, HttpServletResponse response, Object handler,
            ModelAndView modelAndView) throws Exception {
        // TODO Auto-generated method stub

    }

    @Override
    public void afterCompletion(HttpServletRequest request, HttpServletResponse response, Object handler, Exception ex)
            throws Exception {
        // TODO Auto-generated method stub

    }

}